n1 = int(input("Informe o primeiro número: "))
n2 = int(input("Informe o primeiro segundo: "))
op = input("Informe a operação: ")

mais = n1 + n2
menos = n1 - n2
muti = n1 * n2
divisao = n1 / n2


    
if op == "+":
    print("Resultado é {}".format(mais))
elif op == "-":
    print("Resultado é {}".format(menos))
elif op == "*":
    print("Resultado é {}".format(muti))  
elif op == "/":
    print("Resultado é {}".format(divisao)) 

   
 



